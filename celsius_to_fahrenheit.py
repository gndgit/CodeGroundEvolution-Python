print("\n Python program to convert Celsius to Fahrenheit.\n")

celsius = float(input("Enter Celsius: "))

# Formula : Fahrenheit = (Celsius * 9/5) + 32
Fahrenheit = (celsius * 9/5) + 32
print(f"{celsius} degree celsius is equals to {Fahrenheit} degrees Fahrenheit.")

