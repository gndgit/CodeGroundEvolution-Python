print("\n Question:  Write a Python program to swap two variables?\n")

a = int(input("Enter first (A) number: "))
b = int(input("Enter second (B) number: "))

temp = a
a = b
b = temp

print(f"\nPrevious A Value is {temp},\t Now {a}")
print(f"\nPrevious B Value is {a},\t Now {temp}")

