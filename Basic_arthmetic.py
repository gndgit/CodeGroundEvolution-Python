# Addition Program

print(":Addition, Subtraction, Divide, Multiplication, Modulo Operation:\n")
A = float(input("Enter First Number:\t"))
B = float(input("Enter Second Number:\t"))
Addition = A+B
Subtraction = A-B
Division = A/B
DivisionWhl = A//B
Multiplication = A*B
Modulo = A%B 
ExponentialA = A**2 # A * A
# if A**5 = A*A*A*A*A
ExponentialB = B**2
print(f"Addition  is {A} + {B} = {Addition} ")
print(f"Subtraction  is {A} - {B} = {Subtraction} ")
print(f"Multiplication  is {A} * {B} = {Multiplication} ")
print(f"Division  is {A} / {B} = {Division} ")
print(f"Division(Whole Number)  is {A} // {B} = {DivisionWhl} ")
print(f"Modulo  is {A} % {B} = {Modulo} ")
print(f"Exponential  is {A} ** 2 = {ExponentialA} ")
print(f"Exponential  is {B} ** 2 = {ExponentialB} ")


